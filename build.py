    title = link_tag.text.strip()
    if not title: return None

    # ä½¿ç”¨ç»Ÿä¸€çš„å›¾æ ‡ç”ŸæˆæœåŠ¡ï¼Œé¿å… Base64 å†…å­˜é—®é¢˜
    icon = f"https://ui-avatars.com/api/?background=random&color=fff&name={title[0] if title else 'X'}&size=64"

